package Topics.BTree;

import Libs.Node;

public class BTPopulatingNextRightPointersinEachNodeII {

    /* Easy way to use queue */
    public Node connect(Node root) {
        Node cur = root, head = null, prev = null;
        while (cur != null) {
            while (cur != null) {
                if (cur.left != null) {
                    if (prev != null) {
                        prev.next = cur.left;
                    } else {
                        head = cur.left;
                    }
                    prev = cur.left;
                }
                if (cur.right != null) {
                    if (prev != null) {
                        prev.next = cur.right;
                    } else {
                        head = cur.right;
                    }
                    prev = cur.right;
                }
                cur = cur.next;
            }
            cur = head;
            head = null;
            prev = null;
        }
        return root;
    }
}
